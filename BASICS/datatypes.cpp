#include<iostream>
using namespace std;
int main(){
    // integer data type
    int apples=5;
    cout<<apples<<endl;
    cout<<"size of int:"<<sizeof(apples)<<endl;
    // char data type
    char sign='&';
    cout<<"size of char:"<<sizeof(sign)<<endl;
    // bool datatype
    bool flag =true;
    cout<<flag<<endl;
    cout<<"size of flag:"<<sizeof(flag)<<endl;
    // flag datatype
    float score =3.14;
    cout<<score<<endl;
    cout<<"the size of float:"<<sizeof(score)<<endl;
    return 0;
}